#include "libft.h"
#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	char	*p;
	size_t	i;

	p = (char *)s;
	i = 0;
	while (i < n)
	{
		p[i] = c;
		i ++;
	}
	return (s);
}
